void CodeTabInit(void)
{
    const CodeIndex_typeDef* p;
    p = CodeIndexTab;
    while(1)
    {
        if(p->code != 0xFFFF)
        {
            p++;
            CodeIndex_Length++;
        }else{
            break;
        }
    }
}

const uint8_t* SearchCnCode(uint16_t code)
{
    int16_t low;
    int16_t high;
    int16_t mid;
    const CodeIndex_typeDef* pCodeIndex;
    uint16_t data;
    low = 0;
    high = CodeIndex_Length-1;
    while(low <= high)
    {
        mid = (low+high) >> 1;
        pCodeIndex = &CodeIndexTab[mid];
        data = pCodeIndex->code;
        if(code == data){
            return pCodeIndex->FontHex;
        }else{
            if(code < data){
                high = mid - 1;
            }else{
                low = mid + 1;
            }
        }
    }
    return NULL;
}
